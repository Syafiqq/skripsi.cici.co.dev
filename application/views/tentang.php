                                        <img alt="" class="img-responsive" src="<?php echo base_url(); ?>assets/frontend/images/stroke.jpg" />
                                        <div class="dept-subtitle-tabs">Apa Itu Stroke</div>
                                        <p>Stroke adalah suatu gangguan fungsi otak yang terjadi secara mendadak, disebabkan semata-mata oleh gangguan pembuluh darah di otak, dan dapat mengakibatkan kematian. Umumnya stroke ditandai dengan timbulnya gangguan saraf (defisit neurologis) fokal atau global, yang berlangsung lebih dari 24 jam.</p>
                                    </div>
                                    <div class="tab-pane fade" id="b">
                                        <div class="dept-title-tabs">Hemorrhagic</div>
                                        <img alt="" class="img-responsive" src="<?php echo base_url(); ?>assets/frontend/images/stroke.jpg" />
                                        <div class="dept-subtitle-tabs">Hemorragic</div>
                                        <p>Stroke hemoragik terjadi bila pembuluh darah di dalam otak pecah. Otak sangat sensitif terhadap perdarahan, dan kerusakan dapat terjadi dengan sangat cepat. Pendarahan di dalam otak dapat mengganggu jaringan otak, sehinga menyebabkan pembengkakan, mengumpul menjadi sebuah massa yang disebut hematoma. Pendarahan juga meningkatkan tekanan pada otak dan menekan tulang tengkorak.
                                        </p>
                                    </div>
                                    <div class="tab-pane fade" id="c">
                                        <div class="dept-title-tabs">Ischemic Stroke</div>
                                        <img alt="" class="img-responsive" src="<?php echo base_url(); ?>assets/frontend/images/stroke.jpg" />
                                        <div class="dept-subtitle-tabs">Iskemik</div>
                                        <p>Stroke iskemik terjadi karena  terganggunya aliran darah ke dalam otak akibat dari sumbatan pada pembuluh darah di dalam otak oleh gumpalan darah, sehingga mengakibatkan bagian wajah atau bagian tertentu dalam tubuh tidak bisa di gerakan. Anda harus tahu jika stroke iskemik terjadi ada beberapa gejala yang patut di waspadai seperti Nyeri kepala disertai penurunan kesadaran, bahkan bisa mengalami koma (perdarahan otak). Gangguan penglihatan (mata kabur) pada satu atau dua mata, Gangguan keseimbangan berupa vertigo dan sempoyongan (ataksia),  Kehilangan sebagian atau seluruh kemampuan bicara (afasia), dan Rasa baal pada wajah atau anggota badan satu sisi atau dua sisi. Jika ini terjadi pada anda atau keluarga anda segera lakukan</p>
                                    </div>
                                    <div class="tab-pane fade" id="d">
                                        <div class="dept-title-tabs">Apa Itu T.I.A?</div>
                                        <img alt="" class="img-responsive" src="<?php echo base_url(); ?>assets/frontend/images/stroke.jpg" />
                                        <div class="dept-subtitle-tabs">TIA</div>
                                        <p>TIA adalah episode gangguan aliran darah ke otak secara mendadak, local, singkat, dan menghilang dalam kurun waktu kurang dari 24 jam. Serangan TIA bisa berupa gangguan memori sesaat, kesemutan atau kelumpuhan setengah tubuh, kesulitan berbicara atau mengerti pembicaraan, bicara pelo, gangguan menelan, gangguan penglihatan pada satu atau kedua mata, serta gangguan keseimbangan dan koordinasi. Bila dibiarkan saja tanpa pengobatan, maka kira-kira 30 persennya akan berkembang menjadi stroke, dan 50 persennya terjadi pada tahun pertama, terutama bila penderita mengalami TIA berulang. Oleh karena itu, TIA disebut juga Mini Stroke. Gejala TIA biasanya akan menghilang dalam beberapa menit sampai jam, tetapi tak lebih dari 24 jam. Bila lebih dari 24 jam gejala belum menghilang, maka sudah disebut stroke.</p>
                                    </div>
                                    <div class="tab-pane fade" id="e">
                                        <div class="dept-title-tabs">Fakta Tentang Stroke</div>
                                        <img alt="" class="img-responsive" src="<?php echo base_url(); ?>assets/frontend/images/zzz.jpg" />
                                        <div class="dept-subtitle-tabs">Fakta stroke</div>
                                        <p>1. Stroke hanya menyerang orang tua<br>
                                                Banyak orang yang mempercayai kalau stroke hanya dapat menyerang orang-orang di atas usia 65 tahun. Namun faktanya stroke dapat menyerang semua usia. Kejadian stroke pada anak umumnya disebabkan oleh kelainan komponen darah dan pembuluh darah yang dibawa sejak lahir, dan bukan terkait gaya hidup (kadar kolesterol darah tinggi, kegemukan, dan merokok) seperti pada populasi dewasa.
                                        </p>
                                        <br/>
                                        <p>2. Stroke tidak bisa dicegah<br>
                                                Banyak orang yang merasa pesimis bahwa penyakit stroke tidak bisa dicegah. Padahal faktanya stroke bisa dicegah dengan mengontrol berat badan, tekanan darah, dan kadar kolesterol. Ketiganya bisa didapat dengan cara rajin berolahraga, makan sehat, dan mengurangi konsumsi alkohol serta rokok.</p>
